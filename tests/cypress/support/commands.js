Cypress.Commands.add('uploadMedia', (filePath) => {
    cy.visit('/wp-admin/media-new.php');
    cy.get('.drag-drop').should('exist');
    cy.get('#drag-drop-area').should('exist');
    cy.get('#drag-drop-area').selectFile(filePath, { action: 'drag-drop' });
});